void defaultTask(void *argument) {
  for (;;) {
    g_primary_status_led.toggle();
    osDelay(1000);
  }
}

void externalCommsTask(void *argument) {
  for (;;) {
    g_external_comms.process();
    osDelay(10);
  }
}

