void initHardware() {
  // Reset of all peripherals, Initializes the Flash interface and the Systick.
  HAL_Init();

  /* Initialize all configured peripherals */
  configureSystemClock();
  configureGPIO();
  configureDMA();
  configureDAC();
  configureUART();
  configureADC();
  configureTimer();
  // configureTimers();
}